    private void txtCodigoprocedenciaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoprocedenciaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (Generico.Genericos.verificarTxt(txtCodigoprocedencia)) {
                ObjetoProductos.recuperarRegistro(ControlProductos.opcionesDeRecuperar.recuperarProcedencia, txtDescripcionprocedencia);
                if (Generico.Genericos.verificarTxt(txtDescripcionprocedencia)) {
                    txtPreciocompra.requestFocus();
                }

            }

        }

        if (evt.getKeyCode() == KeyEvent.VK_F1) {
            Operacion objeto = new Operacion();
            new Buscador(this, true, ObjetoProductos.generarResultSet(ControlProductos.opcionesDeRecuperar.recuperarProcedencia), new int[]{0, 1}, objeto).setVisible(true);

            if (objeto.getCodigo() != 0) {
                txtCodigoprocedencia.setText(objeto.getCodigo().toString());
                ObjetoProductos.recuperarRegistro(ControlProductos.opcionesDeRecuperar.recuperarProcedencia, txtDescripcionprocedencia);
                txtPreciocompra.requestFocus();
            }

        }

    }//GEN-LAST:event_txtCodigoprocedenciaKeyPressed

