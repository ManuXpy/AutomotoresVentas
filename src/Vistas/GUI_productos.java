    private void ComboEstadoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ComboEstadoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (Generico.Genericos.verificarCombo(ComboEstado)) {
                int estado;
                estado = ComboEstado.getSelectedIndex();
                if (estado == 1) {
                    txtKm.setText(String.valueOf(0));
                    txtCantidad.requestFocus();

                } else {
                    txtKm.setText(null);
                    txtKm.setEditable(true);
                    txtKm.requestFocus();

                }

            }

        }

    }//GEN-LAST:event_ComboEstadoKeyPressed

