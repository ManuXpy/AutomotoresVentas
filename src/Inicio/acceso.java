    private void btIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIngresarActionPerformed
        if (ObjetoAcceso.verificarUsuario()) {
            new menu().setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuario o Clave Incorrectos");
        }
    }//GEN-LAST:event_btIngresarActionPerformed

    private void btSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalirActionPerformed
        utiles.limpiarCampos(PanelFondo);
        txtUsuario.requestFocus();
    }//GEN-LAST:event_btSalirActionPerformed

    private void txtUsuarioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsuarioKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            if (Generico.Genericos.verificarTxt(txtUsuario)) {
                txtpass.requestFocus();
            }
        }
    }//GEN-LAST:event_txtUsuarioKeyPressed

    private void txtpassKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpassKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            if (Generico.Genericos.verificarTxt(txtpass)) {
                btIngresar.setEnabled(true);
                btIngresar.requestFocus();
            }
        }
    }//GEN-LAST:event_txtpassKeyPressed

